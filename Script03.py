# d/coupage d une chaine de caractere (Slicing)

college = "college bois de boulogne "
print(college[0])
print(college[8])
print(college[-1])
print(college[8:12])
print(college[-9:-1])
print(college[0:20:3])# 3 c est le nombre de saut
print(len(college))# calcule lenth de la phrase
print(college[:]) # la totalite de la variable
print(college[::])# la totalite de la variable
print(college[:-1])
print(college[::-1])# affiche la viariable a l envers